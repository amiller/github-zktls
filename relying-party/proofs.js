// Get N random proofs from the catalog
export function getRandomProofs(n = 5) {
  const all = PROOF_CATALOG.flatMap(site =>
    site.proofs.map(p => ({ ...p, site: site.site, siteName: site.name, icon: site.icon }))
  )
  const shuffled = all.sort(() => Math.random() - 0.5)
  return shuffled.slice(0, n)
}

// Get all proofs for a specific site
export function getProofsForSite(siteDomain) {
  return PROOF_CATALOG.find(s => s.site === siteDomain)
}

// Get total count
export function getTotalProofCount() {
  return PROOF_CATALOG.reduce((sum, site) => sum + site.proofs.length, 0)
}
